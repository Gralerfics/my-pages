            </figure>
            <p>
                All of this is still integer-based. Even though the final board output is only 12-bit RGB, the internal pipeline keeps a higher-precision color representation during blending and only downsamples at the display writeback stage. That tradeoff was necessary because transparent blocks look noticeably worse if the math is collapsed too early.
            </p>
