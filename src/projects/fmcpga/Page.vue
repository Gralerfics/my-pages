                    elsif rising_edge(clk_ppl) and enable = '1' then
                        mode <= mode_next;
                    end if;
                end process;

                mode_next <= not mode when swap_sync = '1' else mode;

                dout_read <= doutb_0 when mode = '0' else doutb_1;
                `"
