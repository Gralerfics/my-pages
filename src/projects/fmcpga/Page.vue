            <h2>Why ray marching fits this project</h2>
            <p>
                For a Minecraft-like scene, rasterizing arbitrary meshes would have made the hardware much heavier than necessary. Here every object is axis-aligned and the world is already a voxel field, so the more natural question for each pixel is simply: starting from the eye position, which block does this ray enter next, and when do I stop?
            </p>
            <p>
                That is why the renderer is built around ray marching. A ray starts from the current view point, advances block by block, checks the next axis crossing, reads the block type, and either keeps going or samples texture color on the intersected face. Transparent blocks are handled by keeping an accumulated RGBA color and continuing until opacity is saturated or the ray exits into the sky.
            </p>
            <figure class="project-media project-media--medium">
